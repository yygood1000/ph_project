    public static boolean isFileArrayExist(String pathStr) {
        if (pathStr != null && !"".equals(pathStr)) {
            String[] pathArray = pathStr.split(";");
            for (int i = 0; i < pathArray.length; i++) {
                if (!isFileExist(pathArray[i])) {
                    return false;
                }
            }
        } else {
            return false;
        }
        return true;
    }

